public class Example2 {

	public static void main(String[] args) {
       //반지름이 5인 원의 면적
		double PI = 3.14;
		int radius = 5;
		
		double gongsic = PI * radius * radius;
		System.out.println(gongsic);
		
	   // 덧셈 : +, 뺄셈 : -, 곱셈 : * , 나눗셈 : /
	   
	   // 밑 면이 4 높이는 4인 삼각형의 넓이를 구하시오.
	   // 밑 면 * 높이 / 2 = 삼각형 넓이.
		
	   int a = 4;
	   int b = 4;
	   
	   int c = a*b/2;
	   
	   System.out.println(c);
	   
	   
			 
		
	
	}

}
